export type ArticleAngle = (typeof ARTICLE_ANGLES)[number]
export type ArticleAngleKey = ArticleAngle['key']

export const ARTICLE_ANGLE_KEYS = ARTICLE_ANGLES.map(
  (a) => a.key
) as ArticleAngleKey[]

export function findArticleAngle(
  key: string | null | undefined
): ArticleAngle | null {
  if (!key) return null
  return ARTICLE_ANGLES.find((a) => a.key === key) ?? null
}

